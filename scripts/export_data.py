# ─── realResults.ts ──────────────────────────────────────────────────────────

def build_real_results():
    """
    Converts data/world_cup_results.csv (fetched from football-data.org)
    into web/src/data/realResults.ts for display in the Grupos tab.
    If the source file doesn't exist, writes an empty array so the build
    always succeeds.
    """
    path = os.path.join(ROOT, "data", "world_cup_results.csv")

    ts = "// AUTO-GENERATED by scripts/export_data.py — do not edit manually\n\n"
    ts += "export interface RealResult {\n"
    ts += "  date: string; homeTeam: string; awayTeam: string;\n"
    ts += "  homeGoals: number; awayGoals: number;\n}\n\n"

    if not os.path.exists(path):
        ts += "export const realResults: RealResult[] = [];\n"
        write_ts(os.path.join(OUT, "realResults.ts"), ts)
        return

    rows = read_csv(path)
    entries = [
        {
            "date":      r["date"],
            "homeTeam":  r["home_team"],
            "awayTeam":  r["away_team"],
            "homeGoals": int(r["home_goals"]),
            "awayGoals": int(r["away_goals"]),
        }
        for r in rows
    ]

    ts += "export const realResults: RealResult[] = "
    ts += json.dumps(entries, ensure_ascii=False, indent=2) + ";\n"
    write_ts(os.path.join(OUT, "realResults.ts"), ts)



# ─── main ────────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    print("Exportando datos a TypeScript...")
    build_real_results()
    build_predictions()
    build_xg()
    build_team_stats()
    build_mejores_terceros()
    build_metadata()
    print("Calculando XG de eliminatorias para todas las combinaciones...")
    build_knockout_xg()
    print("✅ Listo.")
